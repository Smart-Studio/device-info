package com.smartstudio.deviceinfo.model;

public class ScreenInfoViewModel {
    private String model;
    private String manufacturer;
    private String density;
    private String densityCode;
    private String densityX;
    private String densityY;
    private String resolution;
    private String inches;
    private String screenSize;
    private String statusBarHeight;
    private String navBarHeight;
    private String contentHeight;
    private String actionBarHeight;
    private String contentTop;
    private String contentBottom;

    public ScreenInfoViewModel() {
    }

    public String getModel() {
        return model;
    }

    public void setModel(String model) {
        this.model = model;
    }

    public String getManufacturer() {
        return manufacturer;
    }

    public void setManufacturer(String manufacturer) {
        this.manufacturer = manufacturer;
    }

    public String getDensity() {
        return density;
    }

    public void setDensity(String density) {
        this.density = density;
    }

    public String getDensityCode() {
        return densityCode;
    }

    public void setDensityCode(String densityCode) {
        this.densityCode = densityCode;
    }

    public String getDensityX() {
        return densityX;
    }

    public void setDensityX(String densityX) {
        this.densityX = densityX;
    }

    public String getDensityY() {
        return densityY;
    }

    public void setDensityY(String densityY) {
        this.densityY = densityY;
    }

    public String getResolution() {
        return resolution;
    }

    public void setResolution(String resolution) {
        this.resolution = resolution;
    }

    public String getInches() {
        return inches;
    }

    public void setInches(String inches) {
        this.inches = inches;
    }

    public String getScreenSize() {
        return screenSize;
    }

    public void setScreenSize(String screenSize) {
        this.screenSize = screenSize;
    }

    public String getStatusBarHeight() {
        return statusBarHeight;
    }

    public void setStatusBarHeight(String statusBarHeight) {
        this.statusBarHeight = statusBarHeight;
    }

    public String getNavBarHeight() {
        return navBarHeight;
    }

    public void setNavBarHeight(String navBarHeight) {
        this.navBarHeight = navBarHeight;
    }

    public String getContentHeight() {
        return contentHeight;
    }

    public void setContentHeight(String contentHeight) {
        this.contentHeight = contentHeight;
    }

    public String getActionBarHeight() {
        return actionBarHeight;
    }

    public void setActionBarHeight(String actionBarHeight) {
        this.actionBarHeight = actionBarHeight;
    }

    public String getContentTop() {
        return contentTop;
    }

    public void setContentTop(String contentTop) {
        this.contentTop = contentTop;
    }

    public String getContentBottom() {
        return contentBottom;
    }

    public void setContentBottom(String contentBottom) {
        this.contentBottom = contentBottom;
    }
}
